# Usando una sola f-string y print(): muestra un resumen de todas las variables. Ejemplo:
#--- Perfil de usuario --- Nombre: Ivan, Edad: 30, Ciudad: Madrid, Estudiante: False

nombre: str = "Ivan"
edad: int = 30
ciudad: str = "Madrid"
es_estudiante: bool = False

print(f"--- Perfil de usuario --- Nombre: {nombre}, Edad: {edad}, Ciudad: {ciudad}, Estudiante: {es_estudiante}")